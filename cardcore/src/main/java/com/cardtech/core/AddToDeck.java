package com.cardtech.core;
/**
 * Enum with just two values indicating how to add cards back to the deck.
 * 
 * 
 */
public enum AddToDeck {
 /**
  * ADD_TO_TOP - Add to the top of the deck. 
  */
	ADD_TO_TOP,
 /**
  * ADD_TO_BOTTOM - Add to the bottom of the deck. 
  */
	ADD_TO_BOTTOM,
/**
 * ADD_TO_RANDOM - Add at random position in the deck. 
 */
	ADD_TO_RANDOM, 
}
