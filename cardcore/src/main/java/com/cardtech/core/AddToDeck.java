package com.cardtech.core;
/**
 * Enum with values indicating how to add cards back to the deck.
 * 
 */
public enum AddToDeck {
 /**
  * Add to the top of the deck. 
  */
	ADD_TO_TOP,
 /**
  * Add to the bottom of the deck. 
  */
	ADD_TO_BOTTOM,
/**
 * Add at random position in the deck. 
 */
	ADD_TO_RANDOM, 
}
